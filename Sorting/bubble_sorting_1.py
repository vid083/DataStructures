def bubbleSort(arr):
    n = len(arr)
    for i in range(n):
        swapped = False
        for j in range(0, n-i-1):   
            
            if arr[j] > arr[j+1] :
                arr[j], arr[j+1] = arr[j+1], arr[j]
                swapped = True
            print("i=",i,",j=",j,",arr[j]=",arr[j],",arr[j+1]=",arr[j+1],",arr",arr)
        if swapped == False:
            break   


